#เมนู driven Program
#โปรแกรมเมนูหาพื้นที่รูปเรขาคณิต
def main():
    print("โปรแกรมหาพื้นที่รูปเรขาคณิต")
    print("1. หาพื้นที่สี่เหลี่ยมผืนผ้า")
    print("2. หาพื้นที่วงกลม")
    print("3. หาพื้นที่สามเหลี่ยม")
    print("4. หาพื้นที่ทรงกระบอก")
    print("5. หาพื้นที่สี่เหลี่ยมจัตุรัส")
    print("6. หาพื้นที่รูปสี่เหลี่ยมคางหมู")
    print("7. ออกจากโปรแกรม")
    choice = input("กรุณาเลือกเมนู (1-7): ")
    if choice == '1':
        length = float(input("กรุณากรอกความยาวของสี่เหลี่ยมผืนผ้า: "))
        width = float(input("กรุณากรอกความกว้างของสี่เหลี่ยมผืนผ้า: "))
        area = length * width
        print(f"พื้นที่สี่เหลี่ยมผืนผ้าคือ {area} ตารางหน่วย")
    elif choice == '2':
        radius = float(input("กรุณากรอกรัศมีของวงกลม: "))
        area = 3.14 * radius ** 2
        print(f"พื้นที่วงกลมคือ {area} ตารางหน่วย")
    elif choice == '3':
        base = float(input("กรุณากรอกฐานของสามเหลี่ยม: "))
        height = float(input("กรุณากรอกความสูงของสามเหลี่ยม: "))
        area = 0.5 * base * height
        print(f"พื้นที่สามเหลี่ยมคือ {area} ตารางหน่วย")
    elif choice == '4':
        radius = float(input("กรุณากรอกรัศมีของฐานทรงกระบอก: "))
        height = float(input("กรุณากรอกความสูงของทรงกระบอก: "))
        area = 2 * 3.14 * radius * (radius + height)
        print(f"พื้นที่ผิวของทรงกระบอกคือ {area} ตารางหน่วย")
    elif choice == '5':
        side = float(input("กรุณากรอกความยาวด้านของสี่เหลี่ยมจัตุรัส: "))
        area = side ** 2
        print(f"พื้นที่สี่เหลี่ยมจัตุรัสคือ {area} ตารางหน่วย")
    elif choice == '6':
        base1 = float(input("กรุณากรอกความยาวฐานที่ 1 ของรูปสี่เหลี่ยมคางหมู: "))
        base2 = float(input("กรุณากรอกความยาวฐานที่ 2 ของรูปสี่เหลี่ยมคางหมู: "))
        height = float(input("กรุณากรอกความสูงของรูปสี่เหลี่ยมคางหมู: "))
        area = 0.5 * (base1 + base2) * height
        print(f"พื้นที่รูปสี่เหลี่ยมคางหมูคือ {area} ตารางหน่วย")
    elif choice == '7':
        print("ขอบคุณที่ใช้โปรแกรม หวังว่าจะได้พบกันใหม่!")
    else:
        print("ตัวเลือกไม่ถูกต้อง กรุณาเลือกเมนูที่ถูกต้อง (1-7)")
        main()
main()