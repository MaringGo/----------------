#โปรแกรมหาพื้นที่รูปสามเหลี่ยม
#ส่วนที่ 1: เก็บฟังก์ชันที่ใช้บ่อย
def compute_area(b, h): #local variable
    Sd = 0.5 * b * h
    return Sd

#ส่วนที่ 2: ติดต่อข้อมูลกับผู้ใช้
b = float(input("กรุณาใส่ฐานของรูปสามเหลี่ยม: "))
h = float(input("กรุณาใส่ความสูงของรูปสามเหลี่ยม: "))
Sd = compute_area(b, h)
print(f"พื้นที่ของรูปสามเหลี่ยมคือ: {Sd}")